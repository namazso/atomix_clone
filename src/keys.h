/// Possible states of the keys.
///
/// If first bit is set the key is down, if second bit is set it
/// changed state since last tick. This enum contains nice names
/// for this.
enum key_state
{
	KeyState_Up = 0,
	KeyState_Down = 1,
	KeyState_Pressed = 3,
	KeyState_Released = 2,

	KeyFlag_PushState = 1,
	KeyFlag_ChangedState = 2
};
